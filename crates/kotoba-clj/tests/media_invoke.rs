//! End-to-end media: a Clojure-compiled `kotoba-node` component that calls
//! `media-decode` is run on kotoba-runtime's real `WasmExecutor` with a **real
//! MJPEG codec injected via `with_codec`** (ADR-2606272200 §3). This is the full
//! stack in one test: capability/effect (#234) → WIT `media` + `bind_media` (#235)
//! → `codec_fn` hook (#236) → `kotoba-media` MJPEG decode (#237) → a guest frame.
#![cfg(feature = "component")]

use std::collections::HashMap;
use std::sync::Arc;

use kotoba_clj::component::compile_kais_component_str;
use kotoba_runtime::host::{CodecFn, MediaOp, WitQuad};
use kotoba_runtime::WasmExecutor;

const KAIS_WIT_DIR: &str = concat!(env!("CARGO_MANIFEST_DIR"), "/../kotoba-runtime/wit");
const GAS: u64 = 10_000_000;

/// The production-shaped codec: dispatch decode/encode to `kotoba-media`.
fn mjpeg_codec() -> CodecFn {
    Arc::new(|op, codec, bytes| match op {
        MediaOp::Decode => kotoba_media::decode(codec, bytes),
        MediaOp::Encode => kotoba_media::encode(codec, bytes),
    })
}

fn invoke_with_codec(src: &str, input: &[u8], codec: Option<CodecFn>) -> Vec<u8> {
    let component = compile_kais_component_str(src, KAIS_WIT_DIR).expect("compile kotoba-node");
    let exec = WasmExecutor::new(GAS).expect("executor");
    let exec = match codec {
        Some(c) => exec.with_codec(c),
        None => exec,
    };
    exec.execute(
        "clj-media-test",
        &component,
        "did:key:z6MkTestAgent",
        input.to_vec(),
        Vec::<WitQuad>::new(),
        HashMap::<String, String>::new(),
    )
    .expect("execute media guest")
    .output_cbor
}

#[test]
fn media_decode_guest_decodes_real_mjpeg_end_to_end() {
    // Guest receives the JPEG packet, decodes it through the native codec, returns the frame.
    let out = invoke_with_codec(
        r#"(defn run [pkt] (media-decode "mjpeg" pkt))"#,
        TEST_JPEG,
        Some(mjpeg_codec()),
    );
    // out = self-describing frame [w:u32 BE][h:u32 BE][components:u8] ++ pixels
    assert!(out.len() >= 9, "frame carries a 9-byte header");
    let w = u32::from_be_bytes(out[0..4].try_into().unwrap());
    let h = u32::from_be_bytes(out[4..8].try_into().unwrap());
    let comps = out[8];
    assert_eq!((w, h, comps), (7, 5, 3), "decoded 7x5 RGB frame");
    assert_eq!(out.len(), 9 + (w * h * comps as u32) as usize);
}

#[test]
fn media_decode_without_codec_is_opaque_passthrough() {
    // No `with_codec` → `bind_media` falls back to opaque passthrough; the guest gets
    // its bytes back unchanged. Proves the R0 fallback path still works.
    let out = invoke_with_codec(
        r#"(defn run [pkt] (media-decode "mjpeg" pkt))"#,
        b"raw-bytes",
        None,
    );
    assert_eq!(out, b"raw-bytes");
}

